val_a = 4
val_b = 2

# Soma (+)
print(f"{val_a} + {val_b}:", val_a + val_b)

# Subtração (-)
print(f"{val_a} - {val_b}:", val_a - val_b)

# Multiplicação (*)
print(f"{val_a} * {val_b}:", val_a * val_b)

# Divisão (/)
print(f"{val_a} / {val_b}:", val_a / val_b)

# int()
print(f"valor inteiro de {val_a} / {val_b}:", int(val_a / val_b))

# float()
print(f"valor inteiro de {val_a} + {val_b}:", float(val_a + val_b))

# Módulo, resto da divisão (%)
print(f"{val_a} % {val_b}:", val_a % val_b)

# Divisão inteiro (//)
print(f"{val_a} / {val_b}:", val_a // val_b)